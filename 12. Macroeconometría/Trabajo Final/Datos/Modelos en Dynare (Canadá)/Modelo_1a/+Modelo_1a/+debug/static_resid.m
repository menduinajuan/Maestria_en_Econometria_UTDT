function [lhs, rhs] = static_resid(y, x, params)
T = NaN(6, 1);
lhs = NaN(13, 1);
rhs = NaN(13, 1);
T(1) = y(5)^params(4)/params(4);
T(2) = (y(2)-T(1))^(-params(3));
T(3) = 1+params(1)*y(1)/y(3)-params(2);
T(4) = y(3)^params(1);
T(5) = y(6)*T(4);
T(6) = y(5)^(1-params(1));
lhs(1) = y(5)^(params(4)-1);
rhs(1) = (1-params(1))*y(1)/y(5);
lhs(2) = T(2);
rhs(2) = T(2)*y(12)*T(3);
lhs(3) = T(2);
rhs(3) = T(2)*y(12)*(1+y(13));
lhs(4) = y(2)+y(4)+y(9)*(1+y(13)+params(11)*(exp(y(9)-params(10))-1));
rhs(4) = y(1)+y(9);
lhs(5) = y(1);
rhs(5) = T(5)*T(6);
lhs(6) = y(12);
rhs(6) = (1+y(2)-T(1))^(-params(6));
lhs(7) = y(3);
rhs(7) = y(4)+y(3)*(1-params(2));
lhs(8) = log(y(6));
rhs(8) = log(y(6))*params(7)+x(1);
lhs(9) = y(10);
rhs(9) = 100*(y(1)-y(2)-y(4));
lhs(10) = y(11);
rhs(10) = y(10)-y(9)*(y(13)+params(11)*(exp(y(9)-params(10))-1));
lhs(11) = y(7);
rhs(11) = y(1)/y(5);
lhs(12) = y(13);
rhs(12) = params(8);
lhs(13) = y(8);
rhs(13) = (1-params(1))*y(1)/y(5);
end
